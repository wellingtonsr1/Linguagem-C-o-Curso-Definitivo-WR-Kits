int main()
{
	char str[] = "Teste de casa";

	printf("%s", my_strlwr(str));	
	
	return 0;
	
} /* end main */


/*=============================================================================================*/
/* ----- Desenvolvimento das funções ----- */

char *my_strlwr(char *s)
{
	register int i;

	for(i=0; s[i] != '\0'; i++)
            if(s[i] >= 65 && s[i] <= 90) s[i] += 32;
	
	return s;	
				
} /* end my_strlwr */



