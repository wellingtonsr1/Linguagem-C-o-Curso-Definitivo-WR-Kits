{
	char str[] = "teste de casa";

	printf("%s", my_strupr(str));	
	
	return 0;
	
} /* end main */


/*=============================================================================================*/
/* ----- Desenvolvimento das funções ----- */

char *my_strupr(char *s)
{
	register int i;

	for(i=0; s[i] != '\0'; i++)
            if(s[i] >= 97 && s[i] <= 122) s[i] -= 32;
	
	return s;	
				
} /* end my_strupr */



